CREATE SEQUENCE IF NOT EXISTS virtual_service_seq START WITH 1 INCREMENT BY 1;
CREATE TABLE IF NOT EXISTS virtual_service (id INTEGER GENERATED BY DEFAULT AS SEQUENCE virtual_service_seq PRIMARY KEY,
    operationid varchar(250), input longvarchar(250000),url varchar(250),requestType varchar(250),
    contentType varchar(50),method varchar(50), type varchar(50),priority INTEGER,
    output longvarchar(250000), resources varchar(250), httpStatusCode varchar(250),rule longvarchar(250000),
    excludeList varchar(250), availableParamsList varchar(2500), headerParamsList varchar(2500),
    lastUsedDateTime TIMESTAMP, usageCount INTEGER);

